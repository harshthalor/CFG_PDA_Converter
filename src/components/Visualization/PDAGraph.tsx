// Custom node component for PDA states
function StateNode({ data }: NodeProps) {
  const isStart = data.isStart as boolean;
  const isAccept = data.isAccept as boolean;
  const isHighlighted = data.isHighlighted as boolean;
  const label = data.label as string;
  
  return (
    <div className="relative">
      {/* Start state arrow */}
      {isStart && (
        <div className="absolute -left-8 top-1/2 -translate-y-1/2">
          <svg width="24" height="24" viewBox="0 0 24 24">
            <path 
              d="M4 12 L20 12 M14 6 L20 12 L14 18" 
              stroke="#6366f1" 
              strokeWidth="2" 
              fill="none"
            />
          </svg>
        </div>
      )}
      
      {/* State circle */}
      <div 
        className={`
          w-14 h-14 rounded-full flex items-center justify-center
          border-2 transition-all duration-300
          ${isHighlighted 
            ? 'bg-amber-100 border-amber-500 shadow-lg shadow-amber-200 scale-110' 
            : 'bg-white border-gray-300 hover:border-gray-400'
          }
          ${isAccept ? 'ring-2 ring-offset-2 ring-gray-400' : ''}
        `}
      >
        <span className={`text-sm font-medium transition-colors duration-200 ${isHighlighted ? 'text-amber-800' : 'text-gray-700'}`}>
          {label}
        </span>
      </div>
      
      {/* Handles for edges - hidden visually but functional for routing */}
      <Handle type="target" position={Position.Left} className="!opacity-0 !w-2 !h-2" />
      <Handle type="source" position={Position.Right} className="!opacity-0 !w-2 !h-2" />
      <Handle type="target" position={Position.Top} id="top" className="!opacity-0 !w-2 !h-2" />
      <Handle type="source" position={Position.Bottom} id="bottom" className="!opacity-0 !w-2 !h-2" />
      {/* Additional handles for self-loops */}
      <Handle type="source" position={Position.Top} id="loop-out" className="!opacity-0 !w-2 !h-2 !left-[70%]" />
      <Handle type="target" position={Position.Top} id="loop-in" className="!opacity-0 !w-2 !h-2 !left-[30%]" />
    </div>
  );
}

/**
 * Custom Self-Loop Edge Component
 * Renders a beautiful curved self-loop above the node
 */
function SelfLoopEdge({
  id,
  sourceX,
  sourceY,
  targetX,
  targetY,
  label,
  data,
  markerEnd,
}: EdgeProps) {
  const isHighlighted = data?.isHighlighted as boolean;
  const loopOffset = (data?.loopIndex as number || 0) * 25;
  
  // Create a self-loop path using cubic bezier curves
  const loopHeight = 50 + loopOffset;
  const loopWidth = 35;
  
  // The path starts from source, goes up in a loop, and returns to target
  const path = `
    M ${sourceX} ${sourceY}
    C ${sourceX + loopWidth} ${sourceY - loopHeight},
      ${targetX - loopWidth} ${targetY - loopHeight},
      ${targetX} ${targetY}
  `;
  
  // Calculate label position (top of the loop)
  const labelX = (sourceX + targetX) / 2;
  const labelY = sourceY - loopHeight - 5;
  
  return (
    <>
      <BaseEdge
        id={id}
        path={path}
        markerEnd={markerEnd}
        style={{
          stroke: isHighlighted ? '#f59e0b' : '#6b7280',
          strokeWidth: isHighlighted ? 2.5 : 1.5,
          transition: 'stroke 0.2s, stroke-width 0.2s',
        }}
        className={isHighlighted ? 'animate-pulse' : ''}
      />
      {label && (
        <EdgeLabelRenderer>
          <div
            style={{
              position: 'absolute',
              transform: `translate(-50%, -50%) translate(${labelX}px, ${labelY}px)`,
              pointerEvents: 'all',
            }}
            className="nodrag nopan"
          >
            <div
              className={`
                px-2 py-1 rounded text-xs font-mono whitespace-nowrap
                transition-all duration-200
                ${isHighlighted 
                  ? 'bg-amber-100 text-amber-900 font-semibold shadow-md' 
                  : 'bg-gray-50 text-gray-700 border border-gray-200'
                }
              `}
            >
              {label as string}
            </div>
          </div>
        </EdgeLabelRenderer>
      )}
    </>
  );
}

/**
 * Custom Bezier Edge with offset support for multiple edges
 * between the same two nodes
 */
function OffsetBezierEdge({
  id,
  sourceX,
  sourceY,
  targetX,
  targetY,
  sourcePosition,
  targetPosition,
  label,
  data,
  markerEnd,
}: EdgeProps) {
  const isHighlighted = data?.isHighlighted as boolean;
  const offset = (data?.offset as number) || 0;
  
  // Calculate curved path with offset
  const [edgePath, labelX, labelY] = getBezierPath({
    sourceX,
    sourceY: sourceY + offset,
    targetX,
    targetY: targetY + offset,
    sourcePosition,
    targetPosition,
    curvature: 0.25 + Math.abs(offset) * 0.005, // More curve for offset edges
  });
  
  return (
    <>
      <BaseEdge
        id={id}
        path={edgePath}
        markerEnd={markerEnd}
        style={{
          stroke: isHighlighted ? '#f59e0b' : '#6b7280',
          strokeWidth: isHighlighted ? 2.5 : 1.5,
          transition: 'stroke 0.2s, stroke-width 0.2s',
        }}
        className={isHighlighted ? 'animate-pulse' : ''}
      />
      {label && (
        <EdgeLabelRenderer>
          <div
            style={{
              position: 'absolute',
              transform: `translate(-50%, -50%) translate(${labelX}px, ${labelY + offset}px)`,
              pointerEvents: 'all',
            }}
            className="nodrag nopan"
          >
            <div
              className={`
                px-2 py-1 rounded text-xs font-mono whitespace-nowrap
                transition-all duration-200
                ${isHighlighted 
                  ? 'bg-amber-100 text-amber-900 font-semibold shadow-md' 
                  : 'bg-gray-50 text-gray-700 border border-gray-200'
                }
              `}
            >
              {label as string}
            </div>
          </div>
        </EdgeLabelRenderer>
      )}
    </>
  );
}

