// Apply dagre layout to nodes and edges
function getLayoutedElements(
  nodes: Node[],
  edges: Edge[],
  direction: 'TB' | 'LR' = 'LR'
): { nodes: Node[]; edges: Edge[] } {
  const dagreGraph = new dagre.graphlib.Graph();
  dagreGraph.setDefaultEdgeLabel(() => ({}));
  
  const nodeWidth = 80;
  const nodeHeight = 80;
  
  dagreGraph.setGraph({ 
    rankdir: direction,
    nodesep: 80,
    ranksep: 120,
    marginx: 50,
    marginy: 50,
  });
  
  nodes.forEach((node) => {
    dagreGraph.setNode(node.id, { width: nodeWidth, height: nodeHeight });
  });
  
  edges.forEach((edge) => {
    dagreGraph.setEdge(edge.source, edge.target);
  });
  
  dagre.layout(dagreGraph);
  
  const layoutedNodes = nodes.map((node) => {
    const nodeWithPosition = dagreGraph.node(node.id);
    return {
      ...node,
      position: {
        x: nodeWithPosition.x - nodeWidth / 2,
        y: nodeWithPosition.y - nodeHeight / 2,
      },
    };
  });
  
  return { nodes: layoutedNodes, edges };
}

// Format transition label
function formatTransitionLabel(
  inputSymbol: string | null,
  poppedSymbol: string,
  pushedSymbols: string | null
): string {
  const input = inputSymbol || 'ε';
  const push = pushedSymbols || 'ε';
  return `${input}, ${poppedSymbol} → ${push}`;
}

// Group transitions by source-target pair for edge offset calculation
function groupTransitions(pda: PDA): Map<string, typeof pda.transitions> {
  const groups = new Map<string, typeof pda.transitions>();
  
  pda.transitions.forEach(t => {
    const key = `${t.currentState}->${t.nextState}`;
    if (!groups.has(key)) {
      groups.set(key, []);
    }
    groups.get(key)!.push(t);
  });
  
  return groups;
}

export function PDAGraph({ 
  pda, 
  highlightedStates = [], 
  highlightedTransitions = [],
  hoveredOriginId = null,
  onHoverOriginChange
}: PDAGraphProps) {
  const [nodes, setNodes, onNodesChange] = useNodesState<Node>([]);
  const [edges, setEdges, onEdgesChange] = useEdgesState<Edge>([]);
  const layoutAppliedRef = useRef<string | null>(null); // Track which PDA has been laid out
  
  // Memoize node types and edge types to prevent re-renders
  const memoizedNodeTypes = useMemo(() => nodeTypes, []);
  const memoizedEdgeTypes = useMemo(() => edgeTypes, []);
  
  // Generate a stable key for the PDA structure (for layout caching)
  const pdaKey = useMemo(() => {
    if (!pda) return null;
    return JSON.stringify({
      states: pda.states,
      transitions: pda.transitions.map(t => ({
        from: t.currentState,
        to: t.nextState,
      })),
    });
  }, [pda]);

  // Handle edge hover for origin tracking
  const handleEdgeMouseEnter = useCallback((_event: React.MouseEvent, edge: Edge) => {
    if (onHoverOriginChange) {
      onHoverOriginChange(edge.id);
    }
  }, [onHoverOriginChange]);

  const handleEdgeMouseLeave = useCallback(() => {
    if (onHoverOriginChange) {
      onHoverOriginChange(null);
    }
  }, [onHoverOriginChange]);
  
  // Build and layout graph only when PDA structure changes
  useEffect(() => {
    if (!pda || pdaKey === layoutAppliedRef.current) return;
    
    // Create nodes from states
    const newNodes: Node[] = pda.states.map((state) => ({
      id: state,
      type: 'stateNode',
      position: { x: 0, y: 0 }, // Will be set by layout
      data: {
        label: state,
        isStart: state === pda.startState,
        isAccept: pda.acceptStates.includes(state),
        isHighlighted: highlightedStates.includes(state),
      },
    }));
    
    // Group transitions and create edges with proper types
    const transitionGroups = groupTransitions(pda);
    const newEdges: Edge[] = [];
    
    // Also track reverse edges for bidirectional offset
    const bidirectionalPairs = new Set<string>();
    transitionGroups.forEach((_, key) => {
      const [source, target] = key.split('->');
      const reverseKey = `${target}->${source}`;
      if (transitionGroups.has(reverseKey) && source !== target) {
        bidirectionalPairs.add([source, target].sort().join('<->'));
      }
    });
    
    transitionGroups.forEach((transitions, key) => {
      const isSelfLoop = transitions[0].currentState === transitions[0].nextState;
      const [source, target] = key.split('->');
      const isBidirectional = bidirectionalPairs.has([source, target].sort().join('<->'));
      
      transitions.forEach((t, idx) => {
        const isHighlighted = highlightedTransitions.includes(t.id);
        const label = formatTransitionLabel(t.inputSymbol, t.poppedSymbol, t.pushedSymbols);
        
        // Debug: Log transition originId
        if (t.originId) {
          console.log('[PDA Graph] Creating edge from transition:', t.id, 'with originId:', t.originId);
        }
        
        // Calculate offset for multiple edges between same nodes
        const totalEdges = transitions.length;
        let offsetAmount = 0;
        
        if (totalEdges > 1) {
          offsetAmount = (idx - (totalEdges - 1) / 2) * 35;
        } else if (isBidirectional && !isSelfLoop) {
          // Offset single edges in bidirectional pairs
          offsetAmount = source < target ? -20 : 20;
        }
        
        if (isSelfLoop) {
          // Use custom self-loop edge type
          const edge: Edge = {
            id: t.id,
            source: t.currentState,
            target: t.nextState,
            type: 'selfLoop',
            sourceHandle: 'loop-out',
            targetHandle: 'loop-in',
            label,
            animated: isHighlighted, // Animate active edges
            data: {
              isHighlighted,
              loopIndex: idx, // For stacking multiple self-loops
              originId: t.originId, // For origin tracking
            },
            markerEnd: {
              type: MarkerType.ArrowClosed,
              color: isHighlighted ? '#f59e0b' : '#6b7280',
              width: 16,
              height: 16,
            },
          };
          newEdges.push(edge);
        } else {
          // Use custom offset bezier edge type
          const edge: Edge = {
            id: t.id,
            source: t.currentState,
            target: t.nextState,
            type: 'offsetBezier',
            label,
            animated: isHighlighted, // Animate active edges
            data: {
              isHighlighted,
              offset: offsetAmount,
              originId: t.originId, // For origin tracking
            },
            markerEnd: {
              type: MarkerType.ArrowClosed,
              color: isHighlighted ? '#f59e0b' : '#6b7280',
              width: 16,
              height: 16,
            },
          };
          newEdges.push(edge);
        }
      });
    });
    
    // Apply dagre layout
    const { nodes: layoutedNodes, edges: layoutedEdges } = getLayoutedElements(
      newNodes,
      newEdges
    );
    
    setNodes(layoutedNodes);
    setEdges(layoutedEdges);
    layoutAppliedRef.current = pdaKey;
  }, [pda, pdaKey, setNodes, setEdges]); // Note: Don't include highlightedStates/Transitions here
  
  // Update highlighting WITHOUT re-running layout (step-wise visualization)
  useEffect(() => {
    if (!pda || !layoutAppliedRef.current) return;
    
    // Update node highlighting
    setNodes((currentNodes) =>
      currentNodes.map((node) => ({
        ...node,
        data: {
          ...node.data,
          isHighlighted: highlightedStates.includes(node.id),
        },
      }))
    );
    
    // Update edge highlighting and animation
    setEdges((currentEdges) =>
      currentEdges.map((edge) => {
        const isHighlighted = highlightedTransitions.includes(edge.id);
        return {
          ...edge,
          animated: isHighlighted, // Animate only active edges
          data: {
            ...edge.data,
            isHighlighted,
          },
          markerEnd: {
            type: MarkerType.ArrowClosed,
            color: isHighlighted ? '#f59e0b' : '#6b7280',
            width: 16,
            height: 16,
          },
        };
      })
    );
  }, [highlightedStates, highlightedTransitions, pda, setNodes, setEdges]);

  // Update edge styling based on hoveredOriginId (for origin tracking)
  useEffect(() => {
    if (!pda) return;
    
    console.log('[PDA Origin Tracker] hoveredOriginId:', hoveredOriginId);
    
    setEdges((currentEdges) =>
      currentEdges.map((edge) => {
        const isHighlighted = highlightedTransitions.includes(edge.id);
        // Check if this edge should be origin-highlighted
        const isOriginHovered = hoveredOriginId !== null && (
          edge.id === hoveredOriginId ||
          edge.data?.originId === hoveredOriginId
        );
        
        if (isOriginHovered) {
          console.log('[PDA Origin Tracker] Edge highlighted:', edge.id, 'originId:', edge.data?.originId);
        }
        
        // Determine color: origin hover takes precedence over step highlight
        let strokeColor = '#6b7280'; // default gray
        if (isOriginHovered) {
          strokeColor = '#3b82f6'; // bright blue for origin tracking
        } else if (isHighlighted) {
          strokeColor = '#f59e0b'; // amber for step highlight
        }
        
        return {
          ...edge,
          animated: isHighlighted || isOriginHovered,
          data: {
            ...edge.data,
            isHighlighted,
            isOriginHovered,
          },
          style: {
            ...edge.style,
            stroke: strokeColor,
            strokeWidth: isOriginHovered ? 3 : isHighlighted ? 2.5 : 2,
            transition: 'stroke 0.2s ease, stroke-width 0.2s ease',
          },
          markerEnd: {
            type: MarkerType.ArrowClosed,
            color: strokeColor,
            width: 16,
            height: 16,
          },
        };
      })
    );
  }, [hoveredOriginId, highlightedTransitions, pda, setEdges]);
  
  if (!pda) {
    return (
      <div className="h-full flex items-center justify-center bg-gray-50 rounded-lg border-2 border-dashed border-gray-200">
        <div className="text-center text-gray-500">
          <svg className="w-12 h-12 mx-auto mb-3 text-gray-300" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M4 5a1 1 0 011-1h14a1 1 0 011 1v2a1 1 0 01-1 1H5a1 1 0 01-1-1V5zM4 13a1 1 0 011-1h6a1 1 0 011 1v6a1 1 0 01-1 1H5a1 1 0 01-1-1v-6zM16 13a1 1 0 011-1h2a1 1 0 011 1v6a1 1 0 01-1 1h-2a1 1 0 01-1-1v-6z" />
          </svg>
          <p className="font-medium">No PDA to visualize</p>
          <p className="text-sm">Convert a CFG or input a PDA to see the graph</p>
        </div>
      </div>
    );
  }
  
  return (
    <div className="h-full w-full">
      <ReactFlow
        nodes={nodes}
        edges={edges}
        onNodesChange={onNodesChange}
        onEdgesChange={onEdgesChange}
        nodeTypes={memoizedNodeTypes}
        edgeTypes={memoizedEdgeTypes}
        onEdgeMouseEnter={handleEdgeMouseEnter}
        onEdgeMouseLeave={handleEdgeMouseLeave}
        fitView
        fitViewOptions={{ padding: 0.4 }}
        minZoom={0.2}
        maxZoom={2.5}
        proOptions={{ hideAttribution: true }}
        defaultEdgeOptions={{
          type: 'offsetBezier',
        }}
      >
        <Background color="#e5e7eb" gap={16} size={1} />
        <Controls 
          className="!bg-white !border-gray-200 !shadow-sm !rounded-lg"
          showZoom={true}
          showFitView={true}
          showInteractive={false}
        />
        <MiniMap 
          nodeColor={(node) => {
            if (node.data?.isHighlighted) return '#fbbf24';
            if (node.data?.isStart) return '#818cf8';
            if (node.data?.isAccept) return '#34d399';
            return '#d1d5db';
          }}
          className="!bg-white !border-gray-200 !rounded-lg !shadow-sm"
          maskColor="rgba(0, 0, 0, 0.1)"
          pannable
          zoomable
        />
      </ReactFlow>
    </div>
  );
}

