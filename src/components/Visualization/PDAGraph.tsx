// Wrapper with download functionality
interface PDAGraphWithExportProps extends PDAGraphProps {
  containerRef?: React.RefObject<HTMLDivElement | null>;
}

export function PDAGraphWithExport(props: PDAGraphWithExportProps) {
  const containerRef = useRef<HTMLDivElement>(null);
  const [showLatexModal, setShowLatexModal] = useState(false);
  const [latexCode, setLatexCode] = useState('');
  
  const handleExport = useCallback(async (format: 'png' | 'svg') => {
    if (!containerRef.current) return;
    
    const flowElement = containerRef.current.querySelector('.react-flow') as HTMLElement;
    if (!flowElement) return;
    
    try {
      // Dynamic import for html-to-image (only when needed)
      const { toPng, toSvg } = await import('html-to-image');
      
      const downloadImage = (dataUrl: string, extension: string) => {
        const link = document.createElement('a');
        link.download = `pda-graph.${extension}`;
        link.href = dataUrl;
        link.click();
      };
      
      if (format === 'png') {
        const dataUrl = await toPng(flowElement, {
          backgroundColor: '#ffffff',
          quality: 1,
          pixelRatio: 2,
        });
        downloadImage(dataUrl, 'png');
      } else {
        const dataUrl = await toSvg(flowElement, {
          backgroundColor: '#ffffff',
        });
        downloadImage(dataUrl, 'svg');
      }
    } catch (err) {
      console.error('Export failed:', err);
    }
  }, []);
  
  const handleExportLatex = useCallback(() => {
    if (!props.pda) return;
    
    // Generate nodes and edges from PDA for LaTeX generation
    const pda = props.pda;
    const nodes: Node[] = pda.states.map((state, idx) => ({
      id: state,
      type: 'stateNode',
      position: { x: idx * 150, y: 0 }, // Simple left-to-right layout for position hints
      data: {
        label: state,
        isStart: state === pda.startState,
        isAccept: pda.acceptStates.includes(state),
      },
    }));
    
    // Create edges from transitions
    const edges: Edge[] = pda.transitions.map((t) => ({
      id: t.id,
      source: t.currentState,
      target: t.nextState,
      label: formatTransitionLabel(t.inputSymbol, t.poppedSymbol, t.pushedSymbols),
    }));
    
    const code = generateTikZCode(nodes, edges);
    setLatexCode(code);
    setShowLatexModal(true);
  }, [props.pda]);
  
  return (
    <div ref={containerRef} className="h-full w-full relative">
      <PDAGraph {...props} />
      {props.pda && (
        <div className="absolute top-2 right-2 flex gap-2 z-10">
          <button
            onClick={handleExportLatex}
            className="inline-flex items-center gap-1.5 px-3 py-1.5 text-xs font-medium text-gray-600 
              bg-white border border-gray-200 rounded-lg hover:bg-gray-50 hover:border-gray-300 
              transition-colors shadow-sm"
            title="Export to LaTeX TikZ"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M10 20l4-16m4 4l4 4-4 4M6 16l-4-4 4-4" />
            </svg>
            LaTeX
          </button>
          <ExportButton onClick={() => handleExport('png')} format="png" />
          <ExportButton onClick={() => handleExport('svg')} format="svg" />
        </div>
      )}
      <LaTeXModal 
        isOpen={showLatexModal} 
        onClose={() => setShowLatexModal(false)} 
        latexCode={latexCode}
      />
    </div>
  );
}
