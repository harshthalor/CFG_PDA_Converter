/**
 * Converts React Flow nodes and edges to LaTeX TikZ automata code
 */
function generateTikZCode(nodes: Node[], edges: Edge[]): string {
  const lines: string[] = [];
  
  // TikZ preamble
  lines.push('% TikZ Automata - Generated by CFG ↔ PDA Converter');
  lines.push('% Add to preamble: \\usepackage{tikz}');
  lines.push('% Add to preamble: \\usetikzlibrary{automata,positioning,arrows.meta}');
  lines.push('');
  lines.push('\\begin{tikzpicture}[');
  lines.push('  ->,');
  lines.push('  >=Stealth,');
  lines.push('  node distance=3cm,');
  lines.push('  every state/.style={thick, fill=gray!10},');
  lines.push('  initial text={}');
  lines.push(']');
  lines.push('');
  
  // Sort nodes by position for better layout
  const sortedNodes = [...nodes].sort((a, b) => {
    const xDiff = (a.position?.x || 0) - (b.position?.x || 0);
    if (Math.abs(xDiff) > 50) return xDiff;
    return (a.position?.y || 0) - (b.position?.y || 0);
  });
  
  // Generate node definitions
  lines.push('  % States');
  sortedNodes.forEach((node, idx) => {
    const isStart = node.data?.isStart as boolean;
    const isAccept = node.data?.isAccept as boolean;
    const label = escapeLatex(node.data?.label as string || node.id);
    
    // Build state options
    const options: string[] = ['state'];
    if (isStart) options.push('initial');
    if (isAccept) options.push('accepting');
    
    // Position relative to previous node
    let positioning = '';
    if (idx > 0) {
      const prevNode = sortedNodes[idx - 1];
      const dx = (node.position?.x || 0) - (prevNode.position?.x || 0);
      const dy = (node.position?.y || 0) - (prevNode.position?.y || 0);
      
      if (Math.abs(dx) > Math.abs(dy)) {
        positioning = dx > 0 ? `right of=${escapeLatex(prevNode.id)}` : `left of=${escapeLatex(prevNode.id)}`;
      } else {
        positioning = dy > 0 ? `below of=${escapeLatex(prevNode.id)}` : `above of=${escapeLatex(prevNode.id)}`;
      }
      options.push(positioning);
    }
    
    const nodeId = escapeLatex(node.id);
    lines.push(`  \\node[${options.join(', ')}] (${nodeId}) {$${label}$};`);
  });
  
  lines.push('');
  lines.push('  % Transitions');
  
  // Group edges by source-target pair
  const edgeGroups = new Map<string, Edge[]>();
  edges.forEach(edge => {
    const key = `${edge.source}->${edge.target}`;
    if (!edgeGroups.has(key)) {
      edgeGroups.set(key, []);
    }
    edgeGroups.get(key)!.push(edge);
  });
  
  // Generate edge definitions
  edgeGroups.forEach((groupedEdges, key) => {
    const [source, target] = key.split('->');
    const isSelfLoop = source === target;
    
    // Combine labels for edges between same nodes
    const labels = groupedEdges.map(e => escapeLatex(e.label as string || '')).filter(l => l);
    const combinedLabel = labels.join(', ');
    
    const srcId = escapeLatex(source);
    const tgtId = escapeLatex(target);
    
    if (isSelfLoop) {
      lines.push(`  \\path (${srcId}) edge[loop above] node {$${combinedLabel}$} (${tgtId});`);
    } else {
      // Check for bidirectional edges
      const reverseKey = `${target}->${source}`;
      const hasBidirectional = edgeGroups.has(reverseKey);
      
      if (hasBidirectional && source < target) {
        // Only render once for bidirectional, use bend
        lines.push(`  \\path (${srcId}) edge[bend left=15] node[above] {$${combinedLabel}$} (${tgtId});`);
      } else if (hasBidirectional && source > target) {
        lines.push(`  \\path (${srcId}) edge[bend left=15] node[below] {$${combinedLabel}$} (${tgtId});`);
      } else {
        lines.push(`  \\path (${srcId}) edge node[above] {$${combinedLabel}$} (${tgtId});`);
      }
    }
  });
  
  lines.push('');
  lines.push('\\end{tikzpicture}');
  
  return lines.join('\n');
}

/**
 * Escape special LaTeX characters
 */
function escapeLatex(str: string): string {
  if (!str) return '';
  return str
    .replace(/\\/g, '\\textbackslash{}')
    .replace(/_/g, '\\_')
    .replace(/\$/g, '\\$')
    .replace(/#/g, '\\#')
    .replace(/%/g, '\\%')
    .replace(/&/g, '\\&')
    .replace(/\{/g, '\\{')
    .replace(/\}/g, '\\}')
    .replace(/\^/g, '\\^{}')
    .replace(/~/g, '\\~{}')
    .replace(/ε/g, '\\varepsilon');
}

